package it.polimi.se.eliafinazzigrazioli.adrenaline.core.model;

/**
 * The enum Match phase.
 */
public enum MatchPhase {
    /**
     * Initialization match phase.
     */
    INITIALIZATION,
    /**
     * Recruiting match phase.
     */
    RECRUITING,
    /**
     * Map voting match phase.
     */
    MAP_VOTING,
    /**
     * Playing match phase.
     */
    PLAYING,
    /**
     * Final frenzy match phase.
     */
    FINAL_FRENZY,
    /**
     * Final simple match phase.
     */
    FINAL_SIMPLE,
    /**
     * Ended match phase.
     */
    ENDED,
}
